package Strings;

public class StringFunctions {

   public static void main(String args[]) {
       String str = "Amisha";
       String str1 = str.substring(3);//prints index from 3 to end of string
       System.out.println(str1);

       String str2 = str.substring(2,4);//prints from Index 2 to ()4-1
       System.out.println(str2);
   }

}
